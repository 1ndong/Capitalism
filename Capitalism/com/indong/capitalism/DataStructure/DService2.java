package com.indong.capitalism.DataStructure;

public class DService2 {
    private String name;
    private long price;

    public DService2(String name , long price)
    {
        this.name = name;
        this.price = price;
    }

    public String getServiceName()
    {
        return name;
    }

    public long getPrice()
    {
        return price;
    }
}
